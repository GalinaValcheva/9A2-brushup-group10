"""## 12. training and validation functions

"""

def run_one_epoch(model, loader, criterion, optimizer=None, training=False):
    # run one training or evaluation epoch
    if training:
        model.train()
    else:
        model.eval()

    total_loss = 0
    all_labels = []
    all_predictions = []
    all_probabilities = []

    for images, labels in tqdm(loader, leave=False):
        images = images.to(device)
        labels = labels.squeeze().long().to(device)

        if training:
            optimizer.zero_grad()

        with torch.set_grad_enabled(training):
            logits = model(images)
            loss = criterion(logits, labels)

            if training:
                loss.backward()
                optimizer.step()

        probabilities = torch.softmax(logits, dim=1)[:, 1]
        predictions = torch.argmax(logits, dim=1)

        total_loss += loss.item() * images.size(0)
        all_labels.extend(labels.detach().cpu().numpy())
        all_predictions.extend(predictions.detach().cpu().numpy())
        all_probabilities.extend(probabilities.detach().cpu().numpy())

    average_loss = total_loss / len(loader.dataset)
    metrics = compute_binary_metrics(all_labels, all_predictions, all_probabilities)
    metrics["loss"] = float(average_loss)

    return metrics

"""## 13. train CNN with checkpointing


"""

set_seed(SEED)

cnn = SmallCNN(num_classes=2).to(device)
criterion = nn.CrossEntropyLoss()
optimizer = optim.Adam(cnn.parameters(), lr=LEARNING_RATE)

best_cnn_val_auc = -1
history = []
checkpoint_path = OUTPUT_DIR / "best_cnn_model.pt"

for epoch in range(1, EPOCHS + 1):
    print(f"epoch {epoch}/{EPOCHS}")

    train_metrics = run_one_epoch(cnn, train_loader, criterion, optimizer=optimizer, training=True)
    val_metrics = run_one_epoch(cnn, val_loader, criterion, optimizer=None, training=False)

    row = {
        "epoch": epoch,
        "train_loss": train_metrics["loss"],
        "train_accuracy": train_metrics["accuracy"],
        "train_auc": train_metrics["auc"],
        "train_f1": train_metrics["f1"],
        "val_loss": val_metrics["loss"],
        "val_accuracy": val_metrics["accuracy"],
        "val_auc": val_metrics["auc"],
        "val_f1": val_metrics["f1"],
    }

    history.append(row)
    print(row)

    # the best checkpoint is selected using validation AUC
    if val_metrics["auc"] > best_cnn_val_auc:
        best_cnn_val_auc = val_metrics["auc"]
        torch.save({
            "model_state_dict": cnn.state_dict(),
            "epoch": epoch,
            "best_val_auc": best_cnn_val_auc,
            "seed": SEED,
            "batch_size": BATCH_SIZE,
            "learning_rate": LEARNING_RATE,
            "epochs": EPOCHS,
            "data_flag": DATA_FLAG,
        }, checkpoint_path)
        print("saved new best checkpoint")

