"""## 7. shared metric function

The same metrics are used for both models to make the comparison fair.

"""

def compute_binary_metrics(y_true, y_pred, y_prob):
    # accuracy measures overall correctness, f1 is useful when classes are imbalanced (it's the harmonic mean) so for nuance it is included. AUC/confusion matrix too

    return {
        "accuracy": float(accuracy_score(y_true, y_pred)),
        "f1": float(f1_score(y_true, y_pred)),
        "auc": float(roc_auc_score(y_true, y_prob)),
        "confusion_matrix": confusion_matrix(y_true, y_pred).tolist(),
    }

def save_json(data, path):
    with open(path, "w") as f:
        json.dump(data, f, indent=4)

"""## 8. prepare flattened arrays for logistic regression

The baseline receives each 28 by 28 image as a flat vector of 784 pixel values.

"""

train_dataset_raw = DataClass(split="train", download=True)
val_dataset_raw = DataClass(split="val", download=True)
test_dataset_raw = DataClass(split="test", download=True)

x_train = train_dataset_raw.imgs.reshape(len(train_dataset_raw), -1).astype(np.float32) / 255.0
x_val = val_dataset_raw.imgs.reshape(len(val_dataset_raw), -1).astype(np.float32) / 255.0
x_test = test_dataset_raw.imgs.reshape(len(test_dataset_raw), -1).astype(np.float32) / 255.0

y_train = train_dataset_raw.labels.squeeze().astype(int)
y_val = val_dataset_raw.labels.squeeze().astype(int)
y_test = test_dataset_raw.labels.squeeze().astype(int)

print("x_train shape:", x_train.shape)
print("y_train shape:", y_train.shape)

"""## 9. train and tune logistic regression baseline

The validation set is used to choose the best value of `C`. The test set is not used during tuning.

"""

c_values = [0.01, 0.1, 1.0, 10.0]

best_baseline = None
best_c = None
best_val_auc = -1
baseline_validation_results = []

for c in c_values:
    model = Pipeline([
        ("scaler", StandardScaler()),
        ("classifier", LogisticRegression(C=c, max_iter=1000, class_weight="balanced")),
    ])

    model.fit(x_train, y_train)

    val_prob = model.predict_proba(x_val)[:, 1]
    val_pred = model.predict(x_val)
    val_metrics = compute_binary_metrics(y_val, val_pred, val_prob)

    baseline_validation_results.append({"C": c, **val_metrics})
    print("C:", c, "validation auc:", val_metrics["auc"])

    if val_metrics["auc"] > best_val_auc:
        best_val_auc = val_metrics["auc"]
        best_c = c
        best_baseline = model

print("best C:", best_c)
print("best validation AUC:", best_val_auc)

"""## 10. final baseline test evaluation

"""

baseline_test_prob = best_baseline.predict_proba(x_test)[:, 1]
baseline_test_pred = best_baseline.predict(x_test)
baseline_test_metrics = compute_binary_metrics(y_test, baseline_test_pred, baseline_test_prob)

