"""## 11. define a small CNN

This model is intentionally simple and easy to explain. It has two convolutional blocks followed by a small classifier. This is suitable for 28 by 28 grayscale images and can be modified easily during the oral interview.

"""

class SmallCNN(nn.Module):
    # small cnn for 28 by 28 grayscale pneumonia images
    def __init__(self, num_classes=2):
        super().__init__()

        self.features = nn.Sequential(
            nn.Conv2d(1, 16, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2),

            nn.Conv2d(16, 32, kernel_size=3, padding=1),
            nn.ReLU(),
            nn.MaxPool2d(2),
        )

        self.classifier = nn.Sequential(
            nn.Flatten(),
            nn.Linear(32 * 7 * 7, 64),
            nn.ReLU(),
            nn.Linear(64, num_classes),
        )

    def forward(self, x):
        x = self.features(x)
        x = self.classifier(x)
        return x

# tensor shape check (the assignment requires checks as good practices, so we should include them as much as possible)
